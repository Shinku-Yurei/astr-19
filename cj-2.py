# 1. Sum of two floating point numbers
float_a = 10.5
float_b = 4.2
sum_result = float_a + float_b

# 2. Difference between two integers
int_a = 20
int_b = 7
diff_result = int_a - int_b

# 3. Product of a floating point number and an integer
# Note: Multiplying a float by an int always results in a float
prod_float = 5.5
prod_int = 4
prod_result = prod_float * prod_int

# Displaying the results
print(f"Sum: {sum_result} | Type: {type(sum_result)}")
print(f"Difference: {diff_result} | Type: {type(diff_result)}")
print(f"Product: {prod_result} | Type: {type(prod_result)}")